        /// <summary>
        /// Сломал все MVVM, не, ну а вы видели какую простыню надо написать, чтобы Drag&Drop сделать?
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void Window_MouseDown(object sender, MouseButtonEventArgs e)
        {
            if (e.ChangedButton == MouseButton.Left)
                DragMove();
        }
    }
